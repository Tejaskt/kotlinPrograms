fun main() {
    val num = 10
    val num2 = 3

    // Arithmetic operators
    println(num + num2)
    println(num - num2)
    println(num * num2)
    println(num.toFloat() / num2) // here is example of type conversion
    println(num % num2) // modulus operator

    //Relational operators
    // >greater than <less than  >= <= == !=

    // pre / post increment
    var i  = 10
    i++ // post increment : it will print the value and then do the increment in value
    ++i // pre increment : it will first increment the value then print the value


    // Logical operators
    // And && , Or || , Not !
    /*  concept of short-circuiting :
        when the first condition is true in OR operator then it will not check the other statement and simply execute the next statement */

}